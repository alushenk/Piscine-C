int		ft_iterative_factorial(int nb)
{
	int i;

	i = 1;
	if (nb == 0 || nb == 1)
		return (1);
	if (nb < 0 || nb > 12)
		return (0);
	else
	{
		while (nb > 1)
			i *= (nb--);
		return (i);
	}
}
